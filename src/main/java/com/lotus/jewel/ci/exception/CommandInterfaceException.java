package com.lotus.jewel.ci.exception;

public class CommandInterfaceException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -1802365865280125059L;

	public CommandInterfaceException() {
		super();
	}

	public CommandInterfaceException(Throwable cause) {
		super(cause);
	}
	
	public CommandInterfaceException(String message) {
        super(message);
    }

}
